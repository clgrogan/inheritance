public class SortingStrings {
    public static void main(String[] args) {
        String john = "John";
        String amy = "Amy";
        String jane = "Jane";
        System.out.println();
        System.out.println("amy.compareTo(john): " + amy.compareTo(john));
        System.out.println("amy.compareTo(amy): " + amy.compareTo(amy));
        System.out.println("john.compareTo(amy): " + john.compareTo(amy));
        System.out.println("john.compareTo(jane): " + john.compareTo(jane));
        System.out.println();
    }
}
